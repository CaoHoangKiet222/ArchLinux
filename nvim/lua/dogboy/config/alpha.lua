  pattern = "LazyVimStarted",
  callback = function()
    local stats = require("lazy").stats()
    local ms = (math.floor(stats.startuptime * 100 + 0.5) / 100)
    dashboard.section.footer.val = "(ɔ◔︣‿◔︣)ɔ❤  Neovim loaded "
        .. stats.count
        .. " plugins in "
        .. ms
        .. "ms ❤ ʕ•́ᴥ•̀ʔっ"
    pcall(vim.cmd.AlphaRedraw)
  end,
})
